<section class="just">
	<h2><a id="train"></a>By plane</h2>
	<p>Berlin has two international airports, Tegel (TXL) and Schönefeld (SXF). The existence of a third, larger airport called BER is purely in the hallucinating mind of the Mayor of Berlin.<br/>
	Currently, TXL is taking most of the load. In fact, TXL is overloaded - so be prepared for queues, and bring some extra time. SXF isn't as bad, but it's still rather inadequate for the amounts of passengers it's handling.</p>
</section>

<section class="two_columns just">
	<div class="retain">
		<h3><a id="txl"></a>Tegel Airport (TXL)</h3>
		<p><a href="img/pages/hotel.travel/txl.jpg" rel="lightbox"><img class="colimg" src="img/pages/hotel.travel/txl_small.jpg" alt="TXL map" /></a></p>
		<p><a href="http://www.berlin-airport.de/de/_dokumente/reisende/2015-09-29-txl-anfahrt-und-parken-de-en.pdf" target="_blank">TXL area map, original file as PDF</a></p>
		<p>TXL is a purpose-built airport for the former West Berlin, and is on the Western side of West Berlin, whilst the Estrel is on the Eastern side of West Berlin &mdash; you get the idea, it's a trek across most of the densely populated areas of the city. A taxi from the airport to the Estrel Hotel would typically cost you anywhere between €30 and €50, depending on traffic, the type of taxi, TXL surcharge… and you'll have to deal with Berlin taxi drivers.</p>
		<p>However, there are other, more pleasant options:</p>
		<p>TXL has a direct bus to Berlin city centre, appropriately called "TXL". To go to the Estrel Hotel, you require a Zone 'Berlin AB' ticket, which is €2.80 in 2017.</p>
		</p>	
		<p><a href="img/pages/hotel.travel/sumainstation.jpg" rel="lightbox"><img src="img/pages/hotel.travel/sumainstation_small.jpg" class="colimg" alt="S + U Main Station" /></a></p>
		<p><a href="http://www.bvg.de/de/standortplan/9003201" target="_blank">Main Station connection map, original file as PDF</a></p>
		<p>Get off the TXL bus at Washington&shy;platz/<wbr />Haupt&shy;bahnhof - you are now at position #6 in the map above. Cross the station, take exit Europaplatz and cross the street. On the other side, you will reach bus stop #3 for the M41 metrobus. This bus runs every 10 minutes and terminates at the central station, so there usually should be one waiting for you.</p>
		<p>Stay on the M41 until <i>Ziegrastraße</i>. This is going to take another 25 to 30 minutes.</p>
		<p>Total travel time with the TXL/M41 buses is about 1h10 to about 1h30, depending on traffic conditions, which may vary during the day.</p>
		<p>…we told you it was a trek through the whole city, didn't we?</p>
	</div>
	<div class="retain">
		<h3><a id="sxf"></a>Sch&ouml;nefeld Airport (SXF)</h3>
		<p>SXF airport is located in the Southeast, just outside Berlin city limits. While it is closer to the Estrel Hotel than TXL, it also has fewer flights - mainly budget airlines and connections to Eastern European countries.</p>
		<p><a href="img/pages/hotel.travel/sxf.jpg" rel="lightbox"><img src="img/pages/hotel.travel/sxf_small.jpg" class="colimg" alt="SXF map" /></a></p>
		<p><a href="http://www.berlin-airport.de/de/_dokumente/reisende/2016-11-vbb-direktverbindungen-sxf.pdf" target="_blank">SXF connection map, original file as PDF</a></p>
		<p>
			From Schönefeld airport, you can either take a taxi (about €20 to €25) or use the following two trains:
			<ul>
				<li><b>S9</b> - leave at <i>Treptower Park</i> and change to the <b>S41 Ring ↻</b> and get off at the next stop <i>Sonnenallee</i></li>
				<li><b>S45</b> - leave at <i>Neukölln</i> and change to the <b>S42 Ring ↺</b> and get off at the next stop <i>Sonnenallee</i></li>
			</ul>
			This journey requires a 'Berlin ABC' ticket which is €3.40 as of 2017.
		</p>
		<p>	Do not take any of the RB or RE trains, as these will bring you to destinations further away, and getting back to the hotel from there will be a nuisance.</p>
	</div>
</section>

