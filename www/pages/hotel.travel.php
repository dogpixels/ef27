<section class="just">
	<h2><a id="train"></a>By train</h2>
	<div class="two_columns just">
		<div class="retain">
			<p>Since Berlin is a rather large city with many long-distance stations, there are a number of options depending on where your train comes from.</p>
			<p>Despite most trains calling at the central station Berlin Hauptbahnhof, this is most likely not what you want - as it'd take you a good 45 minutes to get to the hotel from there.</p>
			<p>Berlin has a ring railway around the city centre; long distance trains will usually also stop at one of the outlying stations as well. Check your schedule for details.</p>
			<a href="img/pages/hotel.travel/sbahnring_berlin.jpg" rel="lightbox">
				<img src="img/pages/hotel.travel/sbahnring_berlin_small.jpg" class="colimg" alt="S-Bahnring Berlin, Image: cc-by-sa-2.5, Robert Aehnelt" />
			</a><!-- Image cc-by-sa-2.5, credit Robert Aehnelt -->
			<p><a href="https://commons.wikimedia.org/wiki/File:S-Bahn-Ring_Berlin.svg" target="_blank">S-Bahnring Berlin, original file on Wikimedia</a></p>
		</div>
		<div class="retain">	
			<p>From all stations, you need to get on either the <b>S41 Ring ↻</b> or the <b>S42 Ring ↺</b> trains and get off at <i>Sonnenallee</i>. All tickets bearing the destination "BERLIN" are valid for the journey to <i>Sonnenallee</i>.</p>
			<p>
				Long-distance stations are:
				<ul>
					<li>North (Hamburg, Rostock): <i>Berlin Gesundbrunnen</i> S41 Ring ↻</li>
					<li>South (München, Praha, Dresden, Leipzig): <i>Berlin Südkreuz</i> S42 Ring ↺</li>
					<li>East (Warszava, Frankfurt/O): <i>Berlin Ostbahnhof</i>, go back to <i>Ostkreuz</i>, then S41 Ring ↻</li>
					<li>West (Frankfurt a.M., Köln, Hannover): either <i>Berlin Ostbahnhof</i>, take any eastward S3, S5, S7 or S75 train to <i>Ostkreuz</i>, then change there onto S41 Ring ↻ - or leave at <i>Berlin Hauptbahnhof</i> and take Metrobus M41 to <i>Sonnenallee</i> (if your ticket includes Berlin+City travel) or purchase a 'Berlin AB' ticket and get off at <i>Ziegrastraße</i> (see TXL instructions for details!)</li>
				</ul>
			</p>
		</div>
	</div>
</section>

