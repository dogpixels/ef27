<h1>Art Show &ndash; Artist Rules</h1>
<section>
	<h2>Art Show Goal and Basic Rules</h2>
    <p>Eurofurence's Art Show traditionally supports furry artists by giving them a place to display 
    their original works to the public, and possibly sell art.</p>   
    <p>The Art Show is not a commercial event for the convention, or a means for Eurofurence to 
    make money. It is an open platform for the benefit of furry art. However it is also a major event 
    at Eurofurence for the convention's guests to view furry artwork in its original form. Please keep 
    in mind that the Art Show space is limited.</p>  
    <p>Eurofurence respects artistic freedom and expression. Since the show is a very visible and 
    representative part of the convention (and also subjected to strict German laws), we need to put 
    a few limits on the topics that can be displayed.</p>
    <p>You may find the complete guidelines for participation on this page. These should not be considered 
    enforceable laws, though. The Art Show managers have the final word in all affairs of the show. While 
    the managers try to establish and maintain a good working relationship with all artists, time constraints 
    at the convention will not allow lengthy discussions. Play nice and in an adult manner, and they 
    will do the same.</p>
</section>

