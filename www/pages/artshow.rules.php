<section class="just">
	<h2><a class="anchor" name="display" id="display"></a>Display Rules</h3>
    <p>The Eurofurence Art Show has a number of technical requirements and rules governing the
    displays.</p>
</section>

<section class="two_columns just">
    <ol>
      <li>Only the panels, tables, etc., that are provided by Eurofurence are allowed as display space. 
      If you need some kind of special stand, contact us beforehand whether this is possible. It might not
      be feasible to set up e.g. a fursuit rack because of space issues, security issues, or the simple
      possibility that it topples over and damages some art.</li>     
      <li>The display space is meant for art and bidsheets only. Decoration of the panels and tables 
      is restricted to certain unobtrusive objects. See the Artist Guide for details.</li>    
      <li>You must not use any sticky tape beyond what we supply, nor any other tool or sticky stuff
      on the panel. The panel surfaces are pretty delicate, and will be damaged by unauthorized materials.
      (It should be obvious that it is not allowed to write or paint on the panels.)</li>
      <li>Electric appliances beyond simple batteries are not allowed on the panels. We do not provide 
      electricity / cables to the panels. It might be possible to power art objects on tables, if the
      device fulfills electric regulations. Generally we discourage powered displays for safety reasons.
      If you want to display "active" art, please contact us beforehand.</li>  
      <li>Flat art (images, drawings, paintings) for the Art Show must be framed, matted, or 
      mounted by the artist.&nbsp;<strong>A piece of cardboard is not a mat, a clear plastic 
      binder is not a frame. </strong>Please, show your artwork some respect by giving it 
      the proper framing treatment. (Art that is already display-ready, like alubonds or canvas 
      prints, do not require an additional frame of course.)</li>
      <li>For identification purposes, the art should bear a (readable) sticker on the reverse side stating 
      the artist's or agent's name and the name of the piece (Latin alphabet preferred). This is not 
      enforced, however we believe it's a good idea to do so just in case.</li>
	</ol>
</section>

