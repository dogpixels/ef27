<section class="just">
	<h2>Costs and Charges</h2>
	<p>Good news: The EF Art Show is for free. Eurofurence offers the Art Show as a complimentary service 
    to artists. We do not charge a fee for panels or a percentage of the sales. The Art Show is financed by 
    Eurofurence. If you want to support it &ndash; feel free to become a sponsor!<br/>
	Eurofurence never acts as the owner of the artwork. It just handles the art for you, and provides the 
    necessary space, panels, and security personnel. That means:</p>
</section>

<section class="two_columns just">
    <ol>
      <li><strong>There is no insurance involved.</strong> Of course there is security present to guard the Art Show. There 
      is a computer system to keep track of exhibits. And of course the managers will help you identifying 
      the perpetrator in any case of damaged artwork. However, the Art Show cannot be held accountable for 
      matters of actual theft, vandalism, or simple accidents. It is up to you to file claims against 
      identified or unidentified offenders.</li>     
      <li><strong>You have to arrange getting your art to the show by yourself.</strong> The Art Show does not allow 
      mail-in art any more due to problems with customs and the risk of artwork getting lost or damaged. 
      Any cost arising from your transportation or import needs is not covered and has to be paid by yourself.</li>  
      <li>Eurofurence just arranges sales between artists and buyers. Any financial obligations resulting 
      from those sales (like taxes) have to be managed and fulfilled by you. You do not enter a sales 
      contract with Eurofurence or the Eurofurence Art Show, but with the buyer.</li>    
      <li>The art must be prepared for display before getting it to the Art Show at your own cost. 
      Eurofurence always provides panels and hooks for hanging the art, but no further materials.
      The Art Show may supply some standard frames and materials in emergency cases (e.g. if your frame
      breaks during transport) for cost compensation, but these are not guaranteed to be available. 
      In addition, if your art needs special arrangements for hanging, please clear the requirements with the 
      managers beforehand.</li>
    </ol>
</section>

