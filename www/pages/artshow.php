<h1>Eurofurence Art Show</h1>
<section>
	<p>So, you are an artist? A sculptor? A painter? Your topic is furry, and you are looking for the 
    place to show your works to the public? Then the Eurofurence Art Show is just the event for 
    you! The Eurofurence Art Show is one of Europe's major display / sales exhibitions for furry-themed 
    artwork. Everybody who has an interest in furry art is wholeheartedly invited to take part.</p>
</section>   

