		<h2>Bidder Information</h2>
		
		<p>Here, you will find the information for bidding on a piece in the Art Show.</p> 
		
		<p>The most important information: <strong>You must pay cash. Please have sufficient funds 
		(in Euro!) ready at pickup.</strong> Unlike other cons, we only provide the marketplace for 
		the artists and do not sell the art in the convention's name. That means that the artists need 
		to get their money immediately, and we can't go through credit card accounts et al.</p>
		
		<p>Bidders need to register at the Art Show entrance to receive bidsheets and a bidder's number. 
		Please don't wait until the last second to register &ndash; we can handle only so many bidders 
		per hour.</p>
		
