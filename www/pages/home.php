<h2>Newcomer's Guide</h2>
<div class="uk-column-1-2@l">
    <div>
        <h3>General Policies</h3>
        <p>First and foremost, as an attendee of Eurofurence, you <strong>must</strong> have read and agreed to all general policies.<br />Take your time and make sure you understand everything. If in doubt, <a href="https://help.eurofurence.org/contact/security" target="_blank" rel="noreferrer">contact our security department</a> about questions regarding these rules.</p>
        <p>You'll find all policies under <a href="policies">"Policies", section "General Policies"</a>.</p>
        <p>Furthermore, all policies listed under "Event Policies" apply to all attendees who attend certain events or activities at the convention.</p>
    </div>

    <div>
        <h3>Dates &amp; Deadlines</h3>
        <p>There are various dates and deadlines to keep in mind. Generally, you'd want to keep track of registration, booking and payment dates:</p>
        <ul>
            <li><strong>Registration</strong> usually starts around January or February. Exact date and time is being announced at <a href="https://reg.eurofurence.org" rel="noreferrer">reg.eurofurence.org</a>.</li>
            <li>Membership <strong>payment</strong> due dates are provided on <a href="https://reg.eurofurence.org" rel="noreferrer">reg.eurofurence.org</a> and within your registration confirmation emails.</li>
            <li>Check <a href="https://reg.eurofurence.org" rel="noreferrer">reg.eurofurence.org</a> for details on the <strong>hotel</strong> booking process and affiliated dates.</li>
        </ul>
        
        <p><strong>Artists</strong> may additionally need to keep an eye on application deadlines for Art Show and Dealers' Den:</p>
        <ul>
            <li><a href="artshow">Check the Art Show Page for the panel application deadline.</a></li>
            <li><a href="dealersden">Check the Dealers' Den Page for the table application deadline.</a></li>
        </ul>
        
        <p><strong>Fursuiters</strong> may want to keep the fursuit badge registration deadline in mind:</p>
        <ul>
            <li><a href="https://fursuit.eurofurence.org/" target="_blank" rel="noreferrer">fursuit.eurofurence.org</a></li>
        </ul>
    </div>

    <div>
        <h3>Overflow Hotels</h3>
        <p>Getting a spot at the popular main hotel is tough, we know that. Part of our efforts to improve this situation is by providing alternatives; On our <a href="hotel/overflow">Overflow page</a>, we'll point you towards suitable hotels and hostels. Expect this information to be available some weeks before the convention. If you rely on such an option, check the page frequently!</p>
    </div>

    <div>
        <h3>Your Way to Eurofurence</h3>
        <p>You won't be left alone to find your way to the convention. Our <a href="hotel/travel">Travel Guide</a> provides useful information on how to get to Berlin and the Hotel itself, where to park your car, how much it costs, and so on.<br />This page is being updated more frequently as the convention draws near, so make sure to check back for updates!</p>
    </div>

    <div>
        <h3>At the Convention</h3>
        <p>Once arrived at the convention, there are two very first things to do in no particular order:</p>
        <ul>
            <li>Check in with the hotel at the reception and receive your room key.</li>
            <li>Check in to the convention at our Registration Desk on-site and receive your convention badge, Con Book and various other goodies.<br />To find the Registration Desk, <a href="hotel/map">check available maps</a> or ask other attendees or our staff.<br/>Outside of registration hours, you can register at any time at the Security Frontdesk.</li>
        </ul>
        <p>If you ever need to identify our staff, that's easy:</p>
        <ul>
            <li>Orange and red lanyards indicate staff members.</li>
            <li>Blue &amp; Yellow indicate our security personnel - they can not only answer all your questions, but they'll also keep you safe and are able to provide medical help, if need arises.</li>
        </ul>
    </div>

    <div>
        <h3>Working at Eurofurence</h3>
        <p>If you'd like to lend a helping hand at or around the convention, we've got <a href="jobs">a list of open positions to fill</a>. Make sure to check the page frequently, as we keep updating it throughout the entire year.</p>
    </div>

    <div>
        <h3>Artists: Selling at Eurofurence</h3>
        <p>There are generally two ways of selling merchandise at Eurofurence: by auction in the Art Show / Art Auction and directly in the Dealers' Den or Artist Alley. All options except Artist Alley require you to to register beforehand until a given deadline and apply for a panel or table accordingly. Read on for deadlines and application processes:</p>
        <ul>
            <li><a href="artshow">Art Show Page</a></li>
            <li><a href="dealersden">Dealers' Den Page</a></li>
        </ul>
    </div>
</div>

