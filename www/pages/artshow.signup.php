<h1>Art Show &ndash; Artist Signup</h1>
<section>
	<h2>Participation and Invitation</h2>
	
    <p class="retain">Traditionally, Eurofurence sends out invitations to artists who may be interested in participating 
    and/or who had participated before. However, getting an invitation is not a requirement. If you 
    think your art deserves a space in the Art Show, please apply for a panel in the show.</p>
</section>

<hr />

<h2>Requirement</h2>
<section class="two_columns just">
	
	<p class="retain">There are no official minimum quality requirements for artwork, but due to the limited
	panel space, <b>the Art Show managers may restrict the number of panels that are allocated to you</b>.
	You are requested to provide example art and if possible a public gallery in the application.
	If you're a newcomer to an art show, we recommend that you have a good hard look at your art, and 
	decide whether it is of suitable quality to be displayed in the show. The Art Show is no actual 
	contest, but you will inevitably be measured up against other artists by the con's patrons.</p>
	<p class="retain">The emphasis of the Art Show is on art that you created yourself, not just happen to own. If you 
	want to display artwork from others, contact the Art Show managers beforehand for advice. While in certain 
	situations or for certain art pieces this may be permitted, showing others' art should be very limited. 
	The show is not a display for sales of leftover purchases.</p>
	<p class="retain">Of course you can act as an agent for artists who are not able to attend, or appoint an agent who will 
	display your artwork for you. For more information, see below.</p>
	<p class="retain">If you are in doubt regarding your participation or the suitability of your artwork, don't hesitate 
	to ask the Art Show managers.</p>

</section>

<hr />

<h2>The Exhibiting Process</h2>
<section class="two_columns just">
	<p class="retain">The Eurofurence Art Show is handling hundreds of art pieces by a score of artists. To perform
	our service in an efficient manner, we need to keep to a standard process, which on the artist's
	side involves the following steps:</p>
	<ul>
	  <li class="retain">Information: You read through the signup documentation, the rules, and the guide which will
	  cover most of your questions. If you have any special needs, or find an open question, you may
	  ask the Art Show team at any time for clarification.</li>
	  <li class="retain">Signup: You apply for the Art Show by mail, and thereby register. You will need to tell us
	  how many panels and tables you need, and what your age rating is for each of those.</li>
	  <li class="retain">Deadline: You need to have applied by the deadline, which is <em><b>March 31st</b></em>.</li>
	  <li class="retain">Panel distribution: After the deadline, we will look at the numbers of available panels vs.
	  the number of panels applied for, and assign each artist panels. This may or may not the number
	  you have asked for, depending on how much demand there is.</li>
	  <li class="retain">Art production: You will need to make art :-) The art must be in a displayable state (i.e.
	  framed) at the time of the show.</li>
	  <li class="retain">Online database: You will be handed a login for an online database. You need this to enter the
	  required information for each piece of art you want to exhibit, and to print bidsheets.</li>
	  <li class="retain">Getting the art to the show: You may attend Eurofurence yourself and bring the art with you.
	  Otherwise, you will need to appoint an <b>agent</b> who brings the art and picks it up again.</li>
	  <li class="retain">Hanging the art: We will provide you with the panels and hooks for hanging, as well as sticky 
	  tape for the bidsheets. It is up to you or your agent to hang the art with the accompanying
	  bidsheets.</li>
	  <li class="retain">Leave packaging: It is possible that you leave the packaging for the art in the Art Show.
	  If the art is sold, the buyer will receive the packaging for the transport home.</li>
	  <li class="retain">During the Art Show: You don't need to do anything during the Art Show opening times. You may
	  enjoy the convention, or sit in the Dealers' Den (please use the separate application process &ndash;
	  Dealers' tables are not part of the Art Show) for commercial activity. The Art Show will do all
	  the required handling for you, up to the sales.</li>
	  <li class="retain">Picking up unsold art: If there is any art unsold (or not for sale), you or your agent need to
	  pick it up after the show closed.</li>
	  <li class="retain">Picking up revenue: When the sales are done, the artists will receive an immediate payout
	  on site.</li>
	  <li class="retain">Going home.</li>
	</ul>    
	<p class="retain">If necessary, we can deviate from the process; but make sure to read the whole documentation to understand
	how these process items work first before asking for an exception.</p>  

	

</section>

