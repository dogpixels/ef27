<h2>Signup and Application</h2>
		
<section class="two_columns just">
	<ul>
		<li class="retain">Lines with a preceding asterisk (*) are mandatory.</li>
		<li class="retain">The private email address is the one we will use to contact you. Most likely it is the address where the application came from. You may enter more than one address. It is up to you to keep it valid, or inform us of changes.</li>
		<li class="retain">The badge number refers to the person that we will meet at the con, which is either you or your agent. You may give us your badge number later if you are not yet registered with Eurofurence.</li>
		<li class="retain">Contact information is private and will normally not be disclosed. The public contact info that you can enter in the Art Show database will be included with the customer receipts  so a customer is able to contact you for questions. The email address provided with the application is not shown anywhere.</li>
		<li class="retain">Phone is not mandatory but it may come in handy in emergency cases.</li>
		<li class="retain">The email we receive this application from is the one we respond to. Under 'Additional email address' you can add more addresses, e.g. if you want to keep your agent informed, but this field is not mandatory.</li>
		<li class="retain">Note that your address is mandatory. Should there be legal disputes of any sort that cannot be resolved by an email exchange, we may be obliged to pass this address to officials or involved parties. Patrons and customers will not see your address.</li>
		<li class="retain">You may ask for half panels and half tables. Find the panel sizes, shapes, and structures in the Artist Guide section.</li>
		<li class="retain">The number of panels/tables should be what you can fill and wish to fill. We cannot guarantee that this is the number of panels or tables that we can actually allocate to you. Desired panels that we cannot give to you immediately may become available at a later time. See below for a discussion on panel distribution.</li>
		<li class="retain">Your example art should represent the kind of art you wish to display.</li>
		<li class="retain">Check the last four lines like this [x] if applicable.</li>
		<li class="retain">The panels will be distributed after March 31st. If we have not received your application by then, you will end up on the waiting list.</li>
		<li class="retain">Be sure to fill out your application truthfully and correctly. Incomplete applications will be rejected.</li>
	</ul>
