<h1>Art Show &ndash; Bidding Rules</h1>
<section>
	<p>This section will inform you in advance about the bidding process. You don't actually have to take any action before the convention itself; there is no bidder pre-registration.</p>
</section>

<hr />

<section class="uk-column-1-2@l">
    <div>
   	 <h2>Register as Bidder</h2>
   	 
   	 <p>The Eurofurence Art Show does not sell or buy art. It acts solely as mediator for sales from sellers to buyers. Buyers bid with their Eurofurence badge number. However, to do so they need a bid sticker sheet.</p>
   	 
   	 <p>Please don't wait until the last second to pick up your bid stickers &mdash; we can handle only so many bidders per hour. Registration will be available during opening times, but not while closing, so please make sure you got your stickers <i>beforehand</i>.</p>
   	 
   	 <p>It is of course not necessary to get bid stickers if you only want to visit the show.</p>
    </div>
    
    <div>
   	 <h2>How to bid</h2>
   	 
   	 <p><strong>To bid on an exhibit</strong>:</p>
   	 
   	 <ul>
   	   <li>The bidder decides on the <strong>amount in whole Euros</strong> they wish to bid. No fractional values or formulas like &ldquo;three times Pi&rdquo; or similar are allowed.</li>
   	   <li>Preferably a bid sticker with a pre-printed amount is chosen or the bid is written into the box on one of the blank bid stickers containing only the printed bidder number in <strong>easy-to-read digits</strong>.</li>
   	   <li>Bids can only be placed on exhibits with bidsheets that have a bidding field, since those without one are not for sale.</li>
   	   <li>The bid must be at least as high as the minimum bid stated on the bidsheet and higher than any previous bid.</li>
   	   <li>The bidder sticks the bid sticker to the first empty bidding field on the bidsheet.</li>
   	   <li>If there is no free bidding field left, tell the Art Show managers, so an empty bidsheet can be provided.</li>
   	   <li>There is no limit to how often you can bid on any exhibit that is for sale until the Art Show is closed. If you run out of bid stickers, you may obtain a new sheet from the Art Show team.</li>
   	   <li>Bids may only be placed using the official, printed bid stickers handed out by the Art Show!</li>
   	 </ul>
   	 
   	 <p><strong>Important</strong>:</p>
   	 
   	 <ul>
   	   <li>A bid is a valid, binding offer to buy, and must be honored.</li>
   	   <li>A bid sticker must not be removed from a bidsheet, or changed after bidding. In case of mistaken bids, the buyer can inform the Art Show managers, who will at their discretion replace the bidsheet and invalidate the bid. Note that "I don't want that any more" does not count as a mistaken bid.</li>
   	   <li>A buyer must keep their bid sticker sheet safe, to prevent others to bid in their name.</li>
   	   <li>Collusion between artist/agent and buyer is forbidden. Fake bids, bids on one's own exhibit for the purpose of raising the price, or bidding in another's name against that person's will is prohibited.</li>
   	 </ul>
    </div>
    
    <div>  
   	 <h2>Closing the Art Show</h2>
   	 
   	 <p>When the Art Show closes for good, every buyer gets the opportunity for final bids, until no more bids are offered. For this purpose, the Art Show team will close the show panel by panel (in the order of panel numbers) and call for final bids which must be placed on the spot. Once no more bids are offered, the panel counts as closed, the bidsheets are marked off, and no further changes are allowed.</p>
   	 
   	 <p>That means: It is useless to wait with bids until the show closes, or to "guard" an exhibit at closing time. To secure an exhibit, bid early and bid often to outdo competitors!</p>
   	 
   	 <p><strong>Warning</strong>: If people crowd the show, security may deem it necessary to close the room and restrict admittance to prevent damage to the exhibits, or even worse, damage to the health of attendees. This may prevent you from placing final bids. We're sorry for this possible inconvenience.</p>
   	 
   	 <p>If you have no more bids to place during closing, please leave the show to prevent crowding.</p>
    </div>
    
    <div>
   	 <h2>Sales and Artwork Pickup</h2>
   	 
   	 <p>Final winners of the art are announced by bulletin board. The lists will be placed in an easily accessible place, most likely in front of the Art Show entrance.</p>
   	 
   	 <!-- <p>Winners are also notified through the Eurofurence app, if they have this app installed and are signed in with their Eurofurence account.</p> -->
   	 
   	 <p>Buyers must pick up their purchase at the designated art pickup times, and pay cash. Credit cards or cheques of any kind are not accepted. We will try to keep the lines as short as possible.</p>
   	 
   	 <p>If you do not show up to pay for or pick up your items, we will try to contact you, but please spare us this effort, as neither we nor the artist will be very pleased. Be aware that the Art Show will not mail purchased art to you and not paying for your won pieces in time will mean that they may be sold to somebody else! The artists rely on you, so please make sure to check the winners' lists and show up in time for sales!</p>
   	 
   	 <p>If you are sure you have won a piece, but don't find your number on the winners' list or are told at the counter that you haven't won it, contact the Art Show team on site, so we can look into the issue. The Art Show involves a lot of manual work, with a lot of numbers. While we try to double- and triple-check, mistakes are still possible.</p>
    </div>
    
    <div>
   	 <h2>Tips on bidding</h2>
   	 
   	 <ul>
   	   <li>The Art Show offers you "helper sheets" where you can write down the items you have bid on, the panels where they hang, and the amount you have bid. This is useful to keep an overview over the art you were interested in, and the total sum of your bids.</li>
   	   <li>Keep in mind that you may win all the pieces you bid on, and have the necessary cash funds available.</li>
   	   <li>The Art Show supplies wood boards you can carry in the show and write on, as well as pens. Please return these when you leave the show.</li>
   	   <li>If you place a bid, <strong>first</strong> write the bid amount on the sticker (you have that wood board, remember) and <strong>then</strong> pull off the sticker and paste it to the bidsheet. It is difficult to write on the panels if you first paste the bare sticker, and then try to scribble a number, because the panels have lots of holes.</li>
   	   <li>Write legibly. Should go without saying.</li>
   	   <li>If you make a mistake, come to the Art Show counter and ask our staff about it. <strong>Do not change a bidsheet on your own!</strong></li>
   	   <li>We cannot answer specific questions on art pieces beyond the information on the bidsheet. You may be able to contact the artist to ask them personally. Perhaps you can find the artist in the Dealers' Den.</li>
   	   <li>Please do not assume you have won nothing, or won everything. If you have placed any bids, check the winner lists or ask at the sales counter.</li>
   	   <li>Some art comes with packaging; there will be small numbered tickets with the bidsheets. You will receive some packaging with the art at sales time (may be complete boxes or just bubble wrap). This may not be fully sufficient for transport (esp. overseas) but should help you in securing your purchased art a bit better.</li>
   	   <li>Since you bid with your registration number, anonymity is not guaranteed.</li>
   	   <li>And once again, because it is important: <strong>Do not lose your bid stickers!</strong> Someone else may find them and use them to bid in your name.</li>
   	 </ul>
    </div>
</section>

