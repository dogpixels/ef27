<h2>Bidsheets and Selling Art</h2>
<section class="uk-column-1-2@l">
	<p> The Art Show allows you to sell your art. Please note that the show doesn't buy art, or sell art to bidders. The Art Show acts exclusively as a mediator in a contract between artist/owner and buyer (selling on commission).</p>
	
	<p>Each piece of art will have a bidsheet hanging next to the piece which provides information on the artwork, the artist, and more. The bidsheet is used for identification of the art, and for purchasing art. A piece of art is marked as &quot;not for sale&quot; by a special no-sale bidsheet that does not provide the purchase-related part.</p>
	
	<p>If you don't want to sell a piece, you simply use the no-sale bidsheet by marking it as not-for-sale in the online database, which will automatically select the correct bidsheet. This sheet still gives the visitor all relevant information about the art and the artist, but does not set a price or allow bidding on this piece.</p>
	
	<p>If you want to sell your art, you use the sales bidsheet. Here, you need to state a minimum price, which is essentially the smallest sum you want to sell the piece for.</p>
	
	<p>Bidsheets are available through the online database. If you do not have a printer available, you may print them at the convention; however, it is strongly recommended that you print bidsheets before the con to avoid extensive wait times.</p>
	
	<p>Visitors of the art show may bid on &quot;for sale&quot; pieces by pasting a bid sticker on the corresponding bidsheet. The first bid must meet the minimum price, consecutive bids must exceed the previous bid. The piece is sold to the highest bidder at the end of the show.</p>
	
	<p>After the closing of the show, the money for winning bids will be collected from the respective bidders in exchange for the art pieces they won and paid for. Once sales all have concluded the collected money for their sold pieces will be handed out to the artists/owners. This is done for practical reasons: Eurofurence does not act as a buyer or reseller. Eurofurence will try to mediate in cases of problematic purchase, but it is up to the artist to verify and claim their money.</p>
	
	<p>Unsold pieces (having no bid) or &quot;Not for sale&quot; pieces are returned to the owner at the end of the art show.</p>
	
	<p>Please note that any and all additional obligations like sales tax or income tax declarations have to be fulfilled by the artist/owner. Eurofurence expects all sellers to be fully aware of their responsibilities in accordance with applicable law in Germany as well as their country of origin and will neither assist nor advise on these matters.</p>
</section>

