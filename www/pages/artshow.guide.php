<h2>Art Show Images</h2>
<section class="uk-column-1-2@l">
	<p>How does the Art Show look? What are the "panels"? How will art be hung on the panel? Well, it's easier to show than to tell, and one image is worth 1000 words, so let's begin. Click on an image to get a larger version!</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_01.jpg"><img src="img/pages/artshow/artshow_01small.jpg" alt="frame" /></a></div>
	
	<p>This is how a completed panel looks like. Many thanks to AlectorFencer for allowing us to use her setup. That is only one example of how panels may be set up; the actual layout of the art show depends on the ratio of adult and general art, security and convenience issues, and technical factors.</p>   
	
	<p>You can see a complete nook with three panel sides and one table. Depending on the number of panels you got, you may have a nook or a wall to yourself, or you may need to share even the space on one single panel with someone else. There are also panels available of half the width, which are usually only assigned to you, if you have only half a panel in total.</p>
	
	<p>You can not influence the layout of the Art Show or the placement of your panel(s) and/or table(s) beyond the separation of adult and general art. The panels are distributed among artists in a way that keeps artists' panels together. You can contact us if you have special needs; we will try to help you to realize your creative ideas. No guarantees can be given however.</p>  
	
	<p>Tables can be placed in nooks. Note that the table offers less space than the panel, but is the only way to display three-dimensional art that cannot be hung. There is no showcase for your art, so if you have delicate art that must not be touched, better bring a protective showcase with you. It is not allowed to touch the art, but we can't have eyes everywhere, and some people love to look with their fingers.</p>
	
	<p>Do not store boxes under the table like in the photo; we provide a separate space for your packing material that you want to give to the buyer.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_02.jpg"><img src="img/pages/artshow/artshow_02small.jpg" alt="frame" /></a></div>
	
	<p>This is the empty panel that will greet you. The instruction sheet states your name and tells you what to do. Once you're done with the setup, you are asked to return this sheet with your signature and have an Art Show manager inspect your setup and check whether you follow the rules.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_03.jpg"><img src="img/pages/artshow/artshow_03small.jpg" alt="frame" /></a></div>
	
	<p>These are the hooks that are available to hang art. There are different sizes to allow for even large framed pieces to be hung.</p>  
	
	<p>The top part goes into the panel holes. No, they don't slide out. Some of the holes may be blocked by the construction frame behind it, but most are usable.</p>   
	
	<p>And no, you are not supposed to drill new holes, use nails, or glue your art to the panel. We love our panels, so please don't deface them.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_04.jpg"><img src="img/pages/artshow/artshow_04small.jpg" alt="frame" /></a></div>
	
	<p>How can art be hung with these hooks? Obviously you need a bail / an eye on your frame. Most frames come with something like the eye on the left side, screwed to the upper frame. In cheap glass frames, the eye may be part of the connectors that hold glass front and backplate together. Make sure that your frame has an eye (or several) so you can hang it.</p>   
	
	<p>The clamps in the middle and to the right are what the Eurofurence Art Show can offer. These are meant for matted art &mdash; they will not hold anything heavier than a cardboard frame (art will slip out) but for these it works fine.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_05.jpg"><img src="img/pages/artshow/artshow_05small.jpg" alt="frame" /></a></div>
	
	<p>That's how it looks like on the panel.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_06.jpg"><img src="img/pages/artshow/artshow_06small.jpg" alt="frame" /></a></div>
	
	<p>The white pieces of paper on the panel are the bidsheets. Each piece needs a bidsheet. As you can see, they are fairly large (smaller for art that is not for sale though), so you need to include them in your planned layout.</p>    
	
	<p>Without a bidsheet, your audience cannot bid or buy, so it's sort of important to have them.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_07.jpg"><img src="img/pages/artshow/artshow_07small.jpg" alt="frame" /></a></div>
	
	<p>Example number one of what not to do. The unfortunate soul that tried to display this exhibit used adhesive tape instead of a proper eye. Of course, the piece fell down and had to be taken out of the show.</p>   
	
	<p>Obviously this is a standing frame, not a hanging frame &mdash; it has no provisions to hang at all. You can use these frames only on a table. Otherwise, you need to screw an eye on the frame. <b>Adhesive tape does not work</b>.</p>   
	
	<p>Please note that we cannot fix your frames for you during the show, nor do we have the tools to drill and screw frames.</p>
	<div uk-lightbox><a href="img/pages/artshow/artshow_08.jpg"><img src="img/pages/artshow/artshow_08small.jpg" alt="frame" /></a></div>
	
	<p>Example number two of what not to do. Hot-gluing a piece of wire doesn't work either, at least not on wood.</p>  
	
	<p>If you need to work with glue, please test the system at home. Remember, a panel may be rocked or shaken during the show, so "working just so-so" is not good enough. The hanging mechanism must be sturdy enough to survive a certain degree of rough handling.</p>
	
	<p>And that's it! Thank you for your consideration in preparing art for the show, it makes the setup so much smoother and faster if you just need to grab a hook and hang a piece.</p>  
	
	<p>Please consider that while we are in a city, you may not be able to buy a fitting frame on location, especially if you need one for a foreign aspect ratio. Prepare the frame as well as the art. If you come a long way, we do recommend mat frames for easy transport, but be aware of stubbed corners.</p>  
	
	<p>Remember: A clear plastic binder sheet is not a frame! Respect your art, and grace it with a mat frame at least.</p>
</section>

