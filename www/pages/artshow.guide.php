<h1>Art Show &ndash; Artist Guide</h1>
<section>
	<p>This is your comprehensive guide on how to prepare your artwork for display. There aren't many requirements, but some have to be met in order to keep the process smooth for everyone.</p>
</section>

<hr />

<h2>Technical Requirements</h2>
<section class="uk-column-1-2@l">
    <p>The Eurofurence Art Show provides panels and hooks for hanging art as well as <a href="#as-guide-tables">tables</a>. The panels are sturdy wooden constructs with predrilled pegboard sides where hooks can be inserted. All panels are usable from both sides. See below for images!</p>
    
    <p>The managers lay out the panels in the room to meet a number of requirements. The artists will normally not be able to request a certain layout (you can ask to be placed next to a friend; we try to honor these requests, but don't guarantee it). Panels are assigned and cannot be chosen freely.</p>
    
    <p>To hang artwork, it must have a tab at the backside which fits the hooks. Please note that commercially available frames may come with tabs or holders that do not fit these hooks because they are designed for screws or nails. Eurofurence tries to make different hooks and additional clamp holders available; these will be on hand in limited quantities only, though. It is forbidden to paste the artwork to the panel with sticky tape or any other adhesive; this will ruin both the panel and the artwork.</p>
    
    <p>
    <a href="https://www.eurofurence.org/EF15/Panelplan-EF15.pdf" target="_blank"><img src="img/pages/artshow/panel-size-sm.gif" alt="Panels" /></a>
    </p>
    
    <p><strong>Panels may be reserved in half-panel steps.</strong></p>
    <ul>
      <li><strong>a full panel will be 2&nbsp;m by 1&nbsp;m (6.56 by 3.28&nbsp;feet)</strong></li>
      <li><strong>a half panel will be 1&nbsp;m by 1&nbsp;m (3.28 by 3.28&nbsp;feet)</strong></li>
    </ul>
    
    <p>Please remember that the space you reserve must include some clearance between pieces, including space for the bid sheets. Your artwork may not extend beyond the upper, right, or left edge of any panel. It may not interfere with any works displayed by other artists.</p>
    
    <p>Not all holes are suitable for attaching a hook; there is a framework inside the panel construction that blocks especially the outer rim of the panel. Please plan your layout to allow for on-site adjustments.</p>
    
    <p>If you have never done this before, you can try the layout for your art at home: Arrange your framed and matted pieces on the floor, and try out what looks best. This will give you an idea of how much panel space you need to reserve. Look here for a sketch with example sizes: <a href="https://www.eurofurence.org/EF15/Panelplan-EF15.pdf" target="_blank">Panel Layout</a>.</p>
    
    <p>You can not display extremely large pieces of art where a full panel would not suffice to hang it, or extremely heavy framing (wooden frame and glass) where you'd e.g. need two people to lift it. The Art Show is not able to handle such extreme cases.</p>
    
    <p id="as-guide-tables">If you have three-dimensional artwork to display, you need a table instead of (or in addition to) a panel. The email application will allow you to request tables. <strong>Tables may be reserved in half-table steps.</strong></p>
    
    <ul>
      <li><strong>a full table will be 130&nbsp;cm by 65&nbsp;cm (51.2 by 25.6&nbsp;inch)</strong></li>
      <li><strong>a half panel will be 65&nbsp;cm by 65&nbsp;cm (25.6 by 25.6&nbsp;inch)</strong></li>
    </ul>
    
    <p>Note that the panels/tables are unprotected and open; if your art breaks easily, bring a showcase or a cover to discourage patrons who &quot;look with their fingers&quot;. You are responsible for the protection of the artwork yourself.</p>
    
    <p><strong>Additional decoration of panels is restricted.</strong></p>
    
    <p>Panels are meant for the display of artwork, bidsheets and accompanying information. Draping the panels with cloth or paper as background or table-cloth is not allowed. Permanent changes to the panels, like paint, are strictly prohibited and may make you liable for replacement.</p>
    
    <p>You may add artist information, like a box with business cards, to your panel. You may add small items as decoration, but any such items are displayed at the discretion of the Art Show managers, who may ask you to remove them if they do not fit in.</p>
    
    <p>Due to fire hazard, it is not allowed to add any electrical appliance to the panel that runs on more than standard, alkaline batteries (e.g. no lithium batteries or similar). That concerns moving installations, lamps, and electronic displays.</p>
    
    <p>The Eurofurence Art Show managers will try to fulfill all your needs in displaying your work. However, they cannot read minds, and not all requirements can be met on-site, so if you plan something special, get in contact with them as soon as possible.</p>
</section>

