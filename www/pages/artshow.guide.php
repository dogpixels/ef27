<h2>Online Art Database</h2>
<section>
	<div>  
	<p>After getting your panel/table assignment for the Art Show, you will receive your password and account data for the online art database (once that goes online). Through the online system, you can enter your art data and create and print bidsheets. This data is also essential in the administration of the Art Show: it is the basic information on how much art we have, to whom it belongs, and how it is supposed to be handled.</p>
		
	<p>Through that system, your receipts will be printed and your revenue will be calculated. Any data that is not entered before the con has to be entered on site, which takes a lot of time. Thus, please register your art online ahead of the con, and help reduce waiting times at the con itself.</p>	
	</div>
</section>

