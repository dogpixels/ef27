<h2>Packaging</h2>
<section>
	<div>  
	<p>You can leave the packaging with the Art Show during the exhibition.</p>
		
	<p>During setup, you will be given pairs of numbered labels to connect art and packaging, one of which must be hung next to the bidsheet and the other one (with the same number) stuck to the packaging. This allows us to store the packaging elsewhere.</p>
		
	<p>You need to bundle your packaging in a way that we can easily see what parts belong together (e.g. styrofoam padding, a plastic sheet, and a box). Otherwise we are not able to match the packaging (parts) with the exhibit.</p>
		
	<p>A buyer will get the packaging handed out together with the art. If you do not want that, please take the packaging with you.</p>
		
	<p>When picking up unsold art, you will be handed out the packaging again together with the exhibit.</p>
		
	<p>Unlabeled packaging that can not be matched with an exhibit will be discarded.</p>
	</div>
</section>

