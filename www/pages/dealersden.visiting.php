<h1>Visiting the Dealers’ Den</h1>

<section class="uk-column-1-2@l">
	<p>Welcome to the Eurofurence Dealers' Den! This is your ultimate guide to dive into this creative realm, where imagination knows no bounds. Here, talented artists, creators, and vendors come together to create a vibrant experience of creativity and passion. Whether you're looking for custom art, merchandise, or something else entirely, you'll find it here!</p>

	<div>
		<h3>What is the Dealers’ Den?</h3>
		<p>The Dealers' Den is the convention’s marketplace, that brings together over 150 of the furry fandom’s most talented artists and creators from Europe and beyond! This is the place where you can explore a diverse range of offerings, from furry merchandise and exclusive prints to custom commissions and masterfully crafted fursuits and much more. Whether you're an experienced Eurofurence Attendee or a curious adventurer stepping in for the first time, treasures await at every corner.</p>
		<p>But that's not all! The Dealers’ Den is also the home of the Eurofurence Con Store, where you can grab the official convention T-shirt and other lovely souvenirs from this year and years past. It's your one-stop shop for all things Eurofurence, combining the talents and creativity of the community with the spirit of the convention.</p>
	</div>

