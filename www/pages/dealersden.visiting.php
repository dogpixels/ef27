		<h3>Additional Information</h3>
		<ul>
			<li><strong>Payment Methods:</strong> The majority of dealers are equipped to accept credit card payments. Alternatives like Cash, PayPal and even Stripe can also be available. For a seamless purchase experience, kindly inquire about your preferred payment method before making a transaction.</li>
			<li><strong>Accessibility:</strong> The Dealers' Den is wheelchair accessible. If an Attendee has a "Handicapped ID" (Behindertenausweis) with the German signs außergewöhnlich gehbehindert ("aG"), blind ("Bl") or hilflos ("H") or they are (without handicapped ID) disabled in regards of walking or standing (i.e. after an operation or because of an injury such as an broken foot/leg) or in need of a wheelchair the Attendee is not required to queue themselves at all for the Dealers’ Den. The Eurofurence Security will let them enter directly into the Dealers’ Den during all opening hours.</li>
			<li>
				<strong>Visitor Etiquette and Rules:</strong> Before diving into the Dealers' Den, please take a moment to familiarize yourself with our Art & Crafts Rules of the House & Etiquette guidelines, which are detailed on the two posters displayed below.
