<h1>Dealers' Den</h1>
<section>
	<h2>Opening Times</h2>
	<p>The Dealers' Den will be open to customers during Eurofurence from <strong>Thursday to Saturday, in the afternoon from 12 pm to 6 pm</strong>. Dealers will be able to setup and tear down their points of sale outside of these times. The exact times will be announced in the conbook. Outside of the opening times the room will be locked, allowing you to leave your table and merchandise in relative safety overnight if you wish. Eurofurence e.V. as the host of the convention cannot be held responsible for any damaged, lost or stolen goods or money, and it is entirely up to you to take reasonable precautions yourself.</p>
</section>

<hr />

<section class="uk-column-1-2@l">
		<div>
			<h3>How do I register as a dealer?</h3>
			<p>To apply for dealer space, you must fill out our form at </strong><a href="https://dealers.eurofurence.org/" target="_blank"><strong>dealers.eurofurence.org</strong></a> starting on the <em>15th of December <?= $this->current->year - 1 ?>, ending on the 31st of December <?= $this->current->year - 1?>!</em></p>			
			<p>The tables will be assigned before the registration for Eurofurence opens. To attend the Den you will still need a valid con ticket by <em>1st of March <?= $this->current->year ?></em> or your table will be assigned to someone on the waiting list.
			You can apply for the Dealers'Den after the deadline at any time but the risks ending up on the waiting list are very high.</p>
			<p>Table sizes are:</p>
			<ul>
				<li>half table (90x80cm / 35.5"x31.5")</li>
				<li>single table (180x80cm / 71"x31.5") &rarr; common</li>
				<li>large table (270x80cm / 106.5"x31.5")</li>
				<li>double table (360x80cm / 141.5"x31.5") &rarr; for two or more artists/dealers</li>
			</ul>
			<p>You will get an e-mail notification once we have received your application. <strong>This notification is NOT a confirmation for the Dealers' Den nor a confirmation that you have pre-registered for the convention itself!</strong></p>
			<p>Registered dealers will receive a written confirmation of any table space that has been assigned to them, together with the first layout of the seating plan, within two weeks after this deadline. <strong>Dealers must pay their convention fee in full before the 1st of March 2020 to become eligible for any table space.</strong> Your registration date neither influences the amount of table space nor the change of any space being allocated to you at all.</p>
			<p>Dealers registering after the deadline will be put on the waiting list and processed on short notice as space permits.</p>
		</div>
		<div>
			<h3>How does the table thing work?</h3>
			<p>Tables are <i>not</i> assigned on a first come, first served basis. All available table space will be distributed among all applicants within two weeks after the dealer registration deadline. The time of your registration neither influences the amount of table space nor the chance of getting any table space at all. The deadline for dealer registrations is the <strong>31st of December</strong>, later applications will be put on the waiting list. All dealers will be informed via e-mail and the seating plan will be <a href="https://forum.eurofurence.org/" target="_blank">published in the Eurofurence forum</a>.</p>
			<p>If you can't make it to Eurofurence or decide against participating in the Dealer's Den after all, please inform us in time so another artist or dealer gets the chance to promote their goods. </p>
		</div>
		<div>
			<h3>What If I want a table together with someone?</h3>
			<p>It is possible to share your table with a friend or fellow artist. There are two ways to do so: </p>
			
			<ul>
				<li>One of you applies for the space you would like to share. (If your share comes after the registration was closed and want to be named in the seating plan as well you can drop us an <a href="https://help.eurofurence.org/contact/dealersden" target="_blank">e-mail</a>.)</li>
				<li>Both of you register and name each other as a "must-have neighbor" in the registration form</li>
			</ul>
			<p>You will find further details at the <a href="https://dealers.eurofurence.org/" target="_blank">Dealer Registration</a>.</p>
		</div>
		<div>	
			<h3>What happens if I don't get a table?</h3>
			<p>Dealers who didn't get a table will be put on the waiting list and as soon as someone cancels her or his registration the table will be given to someone on the waiting list. Dealers and artists who applied via Dealer Registration will be preferred.</p> 
		</div>
		<div>
			<h3>Is there anything I should do once I get a table?</h3>
			<p>Once the tables are assigned, you can log back into the Dealer Registration where you can leave information, teaser images and some data about yourself. We may publish this material in the Eurofurence smart phone app, twitter countdown, etc for some extra commercial.<br/>
			Apart from that, you can start preparing goods to sell.</p>
		</div>
		<div>
			<h3>Is it possible that I get less space than I applied for?</h3>
			<p>Yes, it unfortunately is. Since table space is limited we try to fit in as many artists and dealers as possible without cutting down on the requested space too much.</p>
			<p>If you have any questions regarding the Dealers' Den, please contact the <a href="https://help.eurofurence.org/contact/dealersden" target="_blank">Dealers' Den Management</a>.</p>
		</div>
</section>

