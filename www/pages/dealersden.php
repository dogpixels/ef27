<section class="uk-column-1-2@l">
	<h2>What can I bring?</h2>
		<div>
			<h3>Preferred Merchandise</h3>
			<p>Because we're a furry convention, your goods or services should be of interest to furry fans. We know that it's hard to draw a strict line, so we'll leave it at saying that sellers of furry merchandise will be preferred. Typical items for sale are, for example, (badge) commissions, original furry artwork and prints, CDs and DVDs with artwork, pieces of furry-themed jewellery and fursuit materials as well as plushies made out of faux fur.</p>
		</div>
		<div>
			<p><strong>Fan Art</strong> - that is, art based on TV series, games, or other intellectual property of a third party - can only be displayed and sold subject to some limitations. Ideally, the dealer can provide a legal agreement with the rights holder granting them the right to use the intellectual property in question. Failing this, any use of third-party intellectual property must be within the limits of Fair Use / Free Use or equivalent regulations. In any case, the dealers are responsible that the merchandise they offer does not violate copyright, trade law, or other applicable law. Nevertheless we reserve the right to check your offerings more than once during the convention and may ask you to remove conspicuous material / artwork out of your stock.</p>
		</div>
		<div>
			<p>If you are unsure you can contact the <a href="https://help.eurofurence.org/contact/dealersden" target="_blank">Dealers’ Den Management</a> for further information.</p> 
		</div>
		<div>
			<h3>Prohibited Merchandise</h3>
			<p>The Dealers' Den is an adults-only area, so adult-oriented material may be sold. However, we would like to ask our dealers to apply some common sense. Some items must never be sold:</p>
		</div>	

		<ul>
			<li>Anything that violates German Law, including, but not limited to: weapons, weapon look-alikes, all kind of Nazi symbols, illegal drugs or pornography depicting pedophilia or zoophilia, racist and religious content, all kind of excessive violence or cruelty even in nonsexual content, rape, toilet activities</li>
			<li>Items made of real fur or animal body parts, such as taxidermy animals, clothing or accessories (leather may be excepted).</li>
			<li>Additionally, we do not allow the sale of items that the management of the Dealers' Den deems inappropriate.</li>
			<li>Copyrighted material (see the subject “Fan Art” above) might be displayed but not for sale!</li>
			<li>Hotel regulations prohibit selling food or beverages. The sale of beverages or food in closed containers may be permitted in special cases, with explicit permission from the Dealers' Den Management.</li>
		</ul>
		
		<div>	
			<p>The members of the Dealers' Den team are allowed to check and, if necessary, remove items and artwork from your table at the convention itself if we think it breaches the rules. </p>
			<p>If you are unsure if your merchandise might fall into any of these categories, please contact the <a href="https://help.eurofurence.org/contact/dealersden" target="_blank">Dealers' Den management</a>!</p>
		</div>
		<div>
			<h3>How will everything be handled on site?</h3>
			<p>The Dealers' Den will be open to customers during Eurofurence from Thursday to Saturday, in the afternoon from 12 pm to 6 pm. Dealers will be able to setup and tear down their points of sale outside of these times.</p>
		</div>
		<div>
			<p>A lot of artists use a table cloth or other kind of decoration. You are allowed to frame your table or stack some smaller shelves to enlarge your display space as long as you don't disturb other dealers, artists or customers. You may set up a banner or a display if you don’t disturb other artists or dealers. The emergency exits and the escape ways need to be empty at any time; loud music or machinery disturbing others are also not allowed. Open fire like candles are forbidden!</p>
		</div>
		<div>
			<p>Every dealer and artist needs to sign a declaration waiver. Furthermore, you can name one helper who will get access to the Dealers' Den during setup, breakdown, and opening times to support you. You will also get a handout with all the latest information with your table and the waiver.</p>
		</div>
		<div>
			<p>Outside of the Dealers' Den official opening times, we recommend you cover your table with a table cloth. If you can't bring your own, the Eurofurence Dealers' Den team will help out.</p>
			<p>During the opening times there will always be a contact person from the Dealers' Den team around. </p>
		</div>
