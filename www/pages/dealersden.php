<section>
	<h2>What does it cost?</h2>
	<p>Dealer table space is free of charge! However, we reserve the right to limit the space available for each individual dealer, or reject applications entirely, to balance demand with available space.</p>
	<p>Every registered guest of <strong>the convention may apply for table space, provided the convention fee will be fully paid until 1st of March</strong>.</p>
</section>

